package modulo1;

public class Variabili {

	public static void main(String[] args) {

		//dichiarazione dati
		int a;
		int b;
		double risultato;
		
		//inizializzazione dati
		a = 24; //potrei anche dichiarare e inizializzare tutto in una riga: int a = 10;
		b = 7;
		
		//elaborazione dati
		risultato = (double)a / b;
		
		//restituzione dell'output
		System.out.println(risultato);

	} //fine metodo main()

} //fine classe Variabili
